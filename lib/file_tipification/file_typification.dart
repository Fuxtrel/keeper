  String extractExtension(String file_name) {
    List<String> file_name_parts = file_name.split('.');
    if (file_name_parts.isEmpty) {
      return '';
    }
    String result = '';
    if ((file_name_parts.last == 'gz' || file_name_parts.last == 'zip')) {
      if ((file_name_parts[file_name_parts.length - 2] == 'pages' ||
          file_name_parts[file_name_parts.length - 2] == 'key' ||
          file_name_parts[file_name_parts.length - 2] == 'tar')) {
        result =
        '${file_name_parts[file_name_parts.length -
            2]}.${file_name_parts[file_name_parts.length - 1]}';
      } else {
        return file_name_parts.last;
      }
    } else {
      return file_name_parts.last;
    }
    return result;
  }

  String compareExtension(String extension) {
    if (extension.isEmpty) {
      return 'unexpected';
    }

    if (archives.contains(extension)) {
      return 'archive';
    }

    if (files.contains(extension)) {
      return 'files';
    }

    if (exec.contains(extension)) {
      return 'exec';
    }

    if (settings.contains(extension)) {
      return 'settings';
    }

    if (presentation.contains(extension)) {
      return 'presentation';
    }

    if (audio.contains(extension)) {
      return 'audio';
    }

    if (images.contains(extension)) {
      return 'image';
    }

    if (video.contains(extension)) {
      return 'video';
    }

    if (extension == 'txt') {
      return 'text';
    }
    if (extension == 'pdf') {
      return 'pdf';
    }
    if (extension == 'docx' || extension == 'doc') {
      return 'word';
    }
    if (extension == 'xlsx' || extension == 'xls') {
      return 'exel';
    }
    return 'unexpected';
  }

  String getFilesType(String file_name) {
    return compareExtension(extractExtension(file_name));
  }
